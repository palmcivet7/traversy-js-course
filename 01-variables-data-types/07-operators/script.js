// 1. Arithmetic Operators

let x;
x = 5 + 5; // 10
x = 5 - 5; // 0
x = 5 * 5; // 25
x = 5 / 5; // 1
x = 5 % 5; // 0

// Concatenation
x = 'Hello' + ' ' + 'World'; // Hello World

// Exponent
x = 2 ** 3; // 8

// Increment
x = 1;
x++; // 2

// Decrement
x--; // 1

// Assignment Operators
x = 10; // 10
x += 5; // 15
x -= 5; // 10
x *= 5; // 50
x /= 5; // 1-;
x %= 5; // 0
x **= 5;

// Comparison Operators
x = 2 == 2; // true
x = 2 == '2'; // true
x = 2 == 4; // false
x = 2 === 2; // true
x = 2 === '2'; // false
x = 2 != 1; // true
x = 2 != 2; // false
x = 2 != '2'; // false
x = 2 !== '2'; // true
x = 2 !== 2; // false
x = 10 > 5; // true
x = 10 < 5; // false

console.log(x);
